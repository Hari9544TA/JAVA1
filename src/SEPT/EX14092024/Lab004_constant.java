package SEPT.EX14092024;

public class Lab004_constant {
    public static void main(String[] args) {
        // Constant we have a special key word id final
        // once we declared the value we cant change
        final int a=101;
        System.out.println(a);
        final float PIE=3.14f;
        System.out.println("Value of pie is ="+" "+PIE);

    }
}
