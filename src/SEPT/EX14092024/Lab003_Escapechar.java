package SEPT.EX14092024;

public class Lab003_Escapechar {
    public static void main(String[] args) {
// \n create a new line
        System.out.println("hari"+ '\n'+"good boy");
        // /b backspace
        System.out.println("hari"+ '\b'+"good boy");
        // /t tab create a space b/w the char
        System.out.println("hari"+ '\t'+"good boy");
        // \r carriage return
        System.out.println("hari"+ '\r'+"good boy");
// \f Formfeed
        System.out.println("hari"+ '\f'+"good boy");
        // /' single coute char
        System.out.println("hari\'"+"good boy");
        // \"
        System.out.println("hari\" "+"good boy");
        // \\ back slash
        System.out.println("hari\\"+"good boy");
    }
}
