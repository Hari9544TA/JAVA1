package SEPT.TASK_25092024;

public class Lab010_factorial_program {
    public static void main(String[] args) {

                int n = 5;  // Input value
                int factorial = 1;

                // Using for loop to calculate factorial
                for(int i = 1; i <= n; i++) {
                    factorial *= i;
                }

                // Output the result
                System.out.println("Factorial of " + n + " is: " + factorial);
                }
            }







